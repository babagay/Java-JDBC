package Entity;

public class Customer {

    private int id;
    private String name;
    private String biz;

    public Customer()
    {
    }

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getBiz()
    {
        return biz;
    }

    public void setBiz(String biz)
    {
        this.biz = biz;
    }
}
